test("re-running init for the same footprint updates the registry idempotently", async () => {
  const target = await makeTempDir("code-mux-idempotent-");
  const fakeHome = await makeTempDir("code-mux-home-");

  await runRepoCli(["init", "--ai", "codex", "--ada", "kimi"], {
    cwd: target,
    env: { HOME: fakeHome },
  });
  const firstRegistry = await readRegistry(fakeHome);

  await runRepoCli(["init", "--ai", "codex", "--ada", "kimi"], {
    cwd: target,
    env: { HOME: fakeHome },
  });
  const secondRegistry = await readRegistry(fakeHome);

  assert.equal(firstRegistry.entries.length, 1);
  assert.equal(secondRegistry.entries.length, 1);
  assert.equal(firstRegistry.entries[0].entryId, secondRegistry.entries[0].entryId);
});

test("local and global installs for the same selectors coexist in the registry", async () => {
  const target = await makeTempDir("code-mux-coexist-");
  const fakeHome = await makeTempDir("code-mux-home-");

  await runRepoCli(["init", "--ai", "codex", "--ada", "kimi"], {
    cwd: target,
    env: { HOME: fakeHome },
  });
  await runRepoCli(["init", "--ai", "codex", "--ada", "kimi", "--global"], {
    cwd: target,
    env: { HOME: fakeHome },
  });

  const registry = await readRegistry(fakeHome);
  assert.equal(registry.entries.length, 2);
  assert.deepEqual(
    registry.entries.map((entry) => entry.scope).sort(),
    ["global", "local"],
  );
});
