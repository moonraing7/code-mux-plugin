import test from "node:test";
import assert from "node:assert/strict";
import { mkdir, readFile, realpath, writeFile } from "node:fs/promises";
import { join } from "node:path";
import { makeTempDir, readRegistry, runRepoCli } from "./test-helpers.mjs";

test("list-platforms is stable and tiered", async () => {
  const { stdout } = await runRepoCli(["list-platforms"]);
  const lines = stdout.trim().split("\n");

  assert.equal(lines[0], "host\ttier\tartifacts\tdescription");
  assert.equal(lines[1], "claude\tverified\tskill,command\tClaude Code");
  assert.equal(lines.at(-1), "antigravity\texperimental\tskill\tAntigravity staged skill pack");
});

test("version flags print package version", async () => {
  const packageJson = JSON.parse(
    await readFile(join(process.cwd(), "package.json"), "utf8"),
  );

  const versionFlag = await runRepoCli(["--version"]);
  const versionCommand = await runRepoCli(["version"]);

  assert.equal(versionFlag.stdout.trim(), packageJson.version);
  assert.equal(versionCommand.stdout.trim(), packageJson.version);
});

test("verified local install writes managed Codex skill from cwd with --ai/--ada and updates registry", async () => {
  const target = await makeTempDir("code-mux-local-");
  const fakeHome = await makeTempDir("code-mux-home-");

  await runRepoCli(
    [
      "init",
      "--ai",
      "codex",
      "--ada",
      "gemini",
    ],
    {
      cwd: target,
      env: { HOME: fakeHome },
    },
  );

  const output = join(target, ".codex", "skills", "mux-gemini", "SKILL.md");
  const content = await readFile(output, "utf8");
  const registry = await readRegistry(fakeHome);

  assert.match(content, /generated-by: code-mux/);
  assert.match(content, /Host: `codex`/);
  assert.match(content, /Generated by `code-mux` v\d+\.\d+\.\d+/);
  assert.match(content, /gemini-oriented long-context exploration/i);
  assert.equal(registry.entries.length, 1);
  assert.equal(registry.entries[0].scope, "local");
  assert.equal(registry.entries[0].targetRoot, await realpath(target));
  assert.equal(registry.entries[0].host, "codex");
  assert.equal(registry.entries[0].adapter, "gemini");
});

