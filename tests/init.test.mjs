test("global install resolves into HOME rather than cwd", async () => {
  const fakeHome = await makeTempDir("code-mux-home-");
  const workspace = await makeTempDir("code-mux-work-");

  await runRepoCli(
    [
      "init",
      "--ai",
      "claude",
      "--artifact",
      "command",
      "--ada",
      "kimi",
      "--global",
    ],
    {
      cwd: workspace,
      env: { HOME: fakeHome },
    },
  );

  const output = join(fakeHome, ".claude", "commands", "code-mux", "kimi.md");
  const content = await readFile(output, "utf8");
  const registry = await readRegistry(fakeHome);

  assert.match(content, /Host: `claude`/);
  await assert.rejects(readFile(join(workspace, ".claude", "commands", "code-mux", "kimi.md")));
  assert.equal(registry.entries.length, 1);
  assert.equal(registry.entries[0].scope, "global");
  assert.equal(registry.entries[0].targetRoot, fakeHome);
});

